months = ['jan', 'feb', 'march', 'april', 'may']

# Sort list in alphabetical order
months.sort()
print(months)

# Sort list in reversed alphabetical order
months.sort(reverse=True)
print(months)

# Sort list temporarily
months = ['jan', 'feb', 'march', 'april', 'may']
print(sorted(months))
print(months)

# Reverse a list
months = ['jan', 'feb', 'march', 'april', 'may']
months.reverse()
print(months)
